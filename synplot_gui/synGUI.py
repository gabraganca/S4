#!/usr/bin/python
# -*- coding: utf-8 -*-

"""
Create and interactive synplot.
"""
#==============================================================================

# Import Modules
import sys
from PyQt4 import QtGui

#==============================================================================

#==============================================================================
#
def main():
    
    app = QtGui.QApplication(sys.argv)

    w = QtGui.QWidget()
    w.resize(640, 480)
    w.move(600, 100)
    w.setWindowTitle('synGUI')
    w.show()
    
    sys.exit(app.exec_()) 
#==============================================================================

#==============================================================================
# 
if __name__ == '__main__':
    main()
#==============================================================================
